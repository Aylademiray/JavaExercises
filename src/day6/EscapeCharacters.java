package day6;

public class EscapeCharacters {
    // Escape Characters
    public static void main(String[] args) {
        // Escape Characters
        // symbol \
        System.out.println("symbol \\");

        // symbol \t, tabulation, with four spaces
        System.out.println("    tabulation");
        System.out.println("\ttabulation");

        // symbol \n , new line
        System.out.println("Name: Ali");
        System.out.println("Name:\nAli");

        // Task 1:
        // write a program which ask you for your surname
        // and prints it in new line, use only one
        // System.out.println
        // ex: What is your surname?
        ///////Adams
        System.out.println("What is your surname? \nAdams");
    }
}

