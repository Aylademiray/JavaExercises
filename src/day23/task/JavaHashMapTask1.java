package day23.task;

import java.util.HashMap;

public class JavaHashMapTask1 {
    //1. Write a Java program to associate the
    //   specified value with the specified key in a HashMap.

    public static void main(String[] args) {
        HashMap<Integer, String> map = new HashMap<>();
        map.put(1, "Red");
        map.put(2, "Green");
        map.put(3, "Black");
        map.put(4, "White");
        map.put(5, "Blue");
    }

}
