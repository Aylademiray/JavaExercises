package day10;

public class ForLoops1 {
    public static void main(String[] args) {
        OneMethod1();
        OneMethod2();
        OneMethod3();
    }

    private static void OneMethod1() {
        for (int i = 10; i >= 1; i--) {
            System.out.println("The value of i is: " + i);
        }
    }
    private static void OneMethod2() {
        for (int i = 10; i >= 1; i--) {
            System.out.println("The value of i is: " + i);
        }
    }
    private static void OneMethod3() {
        for (int i = 10; i >= 1; i--) {
            System.out.println("The value of i is: " + i);
        }
    }
}
